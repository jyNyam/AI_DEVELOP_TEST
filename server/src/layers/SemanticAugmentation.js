// ── 내부 헬퍼 ─────────────────────────────────────────────────────────────────
function safeBroadbandItem(item, 의미) {
  if (!item || typeof item !== "object") return { tScore: null, range: null, 의미 };
  return { ...item, 의미 };
}

function buildAnalogies(flaggedScales) {
  return flaggedScales
    .map((name) => {
      const item = ANALOGY_DB[name];
      if (!item) return null;
      return { name, 비유: item.비유, 강점: item.강점 };
    })
    .filter(Boolean);
}

function buildHomecare(flaggedScales) {
  const today     = [];
  const questions = [];

  for (const name of flaggedScales) {
    const guide = HOMECARE_DB[name];
    if (!guide) continue;
    today.push(...guide.오늘당장);
    questions.push(...guide.상담질문);
  }

  return {
    오늘당장: [...new Set(today)].slice(0, 3),
    상담질문: [...new Set(questions)].slice(0, 3),
  };
}

// ── export ────────────────────────────────────────────────────────────────────
export class SemanticAugmentation {
  static augment(cbcData = {}) {
    const scales = {};

    Object.entries(cbcData.scales || {}).forEach(([name, data = {}]) => {
      const semantic = SCALE_SEMANTICS[name] ?? {
        설명: "기타 행동 특성",
        예시: null,
        강점: null,
        노트: "상세 관찰 권장",
      };
      const range = RANGE_SEMANTICS[data.range] ?? {
        위치:  "평가 필요",
        시급성: "확인 권장",
      };

      scales[name] = {
        tScore:  data.tScore ?? null,
        range:   data.range  ?? null,
        의미:    semantic.설명,
        예시:    semantic.예시  ?? null,
        강점:    semantic.강점  ?? null,
        위치:    range.위치,
        시급성:  range.시급성,
        노트:    semantic.노트,
        주목:    ["준임상", "임상"].includes(data.range),
      };
    });

    const broadband     = cbcData.broadband ?? {};
    const flaggedScales = Object.entries(scales)
      .filter(([, v]) => v.주목)
      .map(([k]) => k);

    return {
      아동: {
        나이: cbcData.childAge    ?? "미상",
        성별: cbcData.childGender ?? "미상",
      },
      종합: {
        내재화: safeBroadbandItem(broadband.internalizing, "내면 감정·정서 어려움"),
        외현화: safeBroadbandItem(broadband.externalizing, "외부 행동 문제"),
        전체:   safeBroadbandItem(broadband.total,          "전반적 행동·정서 종합"),
      },
      상세: scales,
      // ── 신규 필드 ──
      analogies: buildAnalogies(flaggedScales),  // 주목 영역의 비유·강점
      homecare:  buildHomecare(flaggedScales),   // 오늘당장 실천 / 상담 질문
      // ──────────────
      가이드: {
        AI역할: "해설자 (진단 아님)",
        금지:   "질환명·처방 언급 금지",
        포함:   "상담사 연결·면책 필수",
      },
    };
  }
}